<template>
  <input class="slider" type="range" :name="name" :min="min" :max="max" :value="value" @change="updateValue">
</template>

<script>
export default {
  name: 'range-slider',

  props: {
    min: Number,
    max: Number,
    value: Number,
    name: String
  },

  methods: {
    updateValue (e) {
      this.$emit('input', e.target.value)
    }
  }
}
</script>

